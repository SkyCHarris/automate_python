# Integer, Floating-Point, and String Data Types

# -Expressions are just values combined with operators, and always evaluate down to a single value
# -A 'data type' is a category for values, and every value belongs to exactly one data type

# Integers | -2, -1, 0, 1, 2, 3, 4, 5
# Floating-point Numbers | -1.25, -1,0, -0.5, 0.0, 0.5, 1.0, 1.25
# Strings | 'a', 'aa', 'aaa', 'Hello!', '11 cats'

# String Concatenation and Replication

# -The meaning of an operator may change based on the data types of the values next to it
# -ex. + can be addition for integers/floats, or concatenation for strings

print('Alice' + 'Bob')

# The * operator can be used for:
# -Multiplying two integer or floating-point values
# -One string value and one ineteger value (string replication operator)

print('Alice' * 5)

